import UIKit

extension UIColor {
    
    /// white Color
    public class var whiteColor: UIColor {
        return UIColor(named: "whiteColor")!
    }
    
    /// blue Color
    public class var blueColor: UIColor {
        return UIColor(named: "blueColor")!
    }
    
    /// background Color
    public class var backgroundColor: UIColor {
        return UIColor(named: "backgroundColor")!
    }
    
    /// black Color
    public class var blackColor: UIColor {
        return UIColor(named: "blackColor")!
    }
}
